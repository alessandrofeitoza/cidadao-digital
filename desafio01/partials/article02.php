        <h2>Como funciona?</h2>
    </header>
    <p>
        Cidadania Digital é um dos temas mais importantes a ser discutido em meio a esta ascensão do acesso a internet e aos meios de comunicação, porém por incrivel que pareça a internet não é um lugar sem lei, ou ao menos não deve ser.
        A internet é uma "Sociedade Digital", e nessa sociedade os integrantes devem seguir alguns direitos e deveres para manter e/ou criar um lugar melhor pra uma convivência virtual.
    </p>

    <p>
        Segundo a pesquisa TIC Kids Online Brasil 2018, no Brasil, 94% dos jovens de 15 a 17 anos estão conectados de alguma forma à internet.
        Sabendo disso a Safernet Brasil lançou um projeto com o intuito de fazer "10 jovens" transformarem e prepararem parte desses 94% de jovens a tornarem a internet um lugar seguro e respeito de se "navegar",
        preparando-os e fortalecendo (ou criando) a consciência de que cada usuário da internet deve (ou deveria) se comportar como Cidadão Digital, promovendo um ambiente colaborativo e democrático.
    </p>
